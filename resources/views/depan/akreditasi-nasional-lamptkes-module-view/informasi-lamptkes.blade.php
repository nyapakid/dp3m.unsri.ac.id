        <h3>Profile LAM-PTKes</h3>
        <p class="text-block text-justify">
            Perkumpulan Lembaga Akreditasi Mandiri Pendidikan Tinggi Kesehatan Indonesia (LAM-PTKes) atau Indonesian Accreditation Agency for Higher 
            Education in Health (IAAHEH) adalah lembaga akreditasi mandiri yang mulai beroperasi pada Maret 2015. Pembentukan LAM-PTKes difasilitasi 
            oleh Direktorat Jenderal Pendidikan Tinggi, Kementerian Pendidikan, dan Kebudayaan melalui Health Professional Education Quality Project 
            (HPEQ Project) dari tahun 2009 sampai 2014.
        </p>

        <p class="text-block text-justify">
            Landasan hukum pendirian LAM-PTKes ialah:<br>
            1. Undang-Undang Republik Indonesia No. 12 tahun 2012 tentang Pendidikan Tinggi, Pasal 55 ayat 5 yang menyebutkan bahwa akreditasi program 
            studi sebagai bentuk akuntabilitas publik dilakukan oleh lembaga akreditasi mandiri;<br>
            2. Peraturan Menteri Riset, Teknologi, dan Pendidikan Tinggi No. 44 Tahun 2015 tentang Standar Pendidikan Tinggi;<br>
            3. Keputusan Menteri Pendidikan dan Kebudayaan No. 291/P/2014 tentang Pengakuan Operasional LAM-PTKes.<br>
        </p>

        <p class="text-block text-justify">
            Setelah HPEQ Project, sebagai lembaga akreditasi mandiri operasional LAM-PTKes didanai melalui biaya akreditasi yang dikumpulkan dari 
            program studi tanpa subsidi dari pemerintah. LAM-PTKes dimonitoring dan dievaluasi setiap tahun oleh Badan Akreditasi Nasional Pendidikan 
            Tinggi (BAN-PT) yang mengembangkan sistem akreditasi nasional. <br>
            <br>
            Salah satu kontribusi penting lainnya adalah fasilitasi dari 7 Organisasi Profesi Kesehatan dan Asosiasi Institusi Kesehatan 
            (Kedokteran, Kedokteran Gigi, Keperawatan, Kebidanan, Gizi, Farmasi, dan Kesehatan Masyarakat) untuk merintis pembentukan Lembaga Akreditasi 
            Mandiri Pendidikan Tinggi Kesehatan Indonesia, yang selanjutnya disebut sebagai Pendiri LAM-PTKes. <br>
            <br>
            Pembentukan LAM-PTKes merupakan tonggak untuk Indonesia karena pertama kalinya lembaga akreditasi mandiri didirikan dan berwenang untuk 
            melakukan akreditasi untuk program studi di berbagai profesi kesehatan. Upaya ini mungkin yang pertama dari jenisnya karena sebagian besar 
            negara memiliki badan akreditasi untuk satu profesi kesehatan tertentu saja. Profesi lain saat ini sedang mempersiapkan untuk mendirikan 
            lembaga akreditasi independen (seperti teknik, psikologi dan akuntansi). Oleh karena itu, LAM-PTKes dipandang sebagai role model untuk 
            profesi lain.

        </p>

