        <h3>Visi, Misi, Tujuan, dan Strategi DP3M</h3>
        <p class="text-block text-justify">
            Sebagai bagian integral dari struktur Universitas Sriwijaya, Direktorat Pengembangan Pendidikan dan Penjaminan Mutu (DP3M) memegang peranan 
            strategis dalam memastikan mutu pendidikan tinggi yang berkelanjutan. Keberadaan DP3M menjadi pilar utama dalam mendukung transformasi Universitas 
            Sriwijaya menjadi Perguruan Tinggi Negeri Berbadan Hukum (PTN-BH) yang unggul, adaptif, dan berdaya saing global. Melalui penguatan sistem pembelajaran, 
            penjaminan mutu internal, serta pengembangan kapasitas sumber daya manusia, DP3M berkomitmen membangun budaya mutu yang kokoh di seluruh unit akademik dan nonakademik.
        </p>
        <br>
        <p class="text-block text-justify">
            Visi, misi, tujuan, dan sasaran strategis (VMTS) DP3M disusun sebagai pedoman arah kebijakan dan rencana pengembangan kelembagaan, yang berorientasi pada peningkatan 
            mutu secara berkesinambungan. Dengan menjadikan siklus PPEPP (Penetapan, Pelaksanaan, Evaluasi, Pengendalian, dan Peningkatan) sebagai dasar operasional, DP3M terus 
            mendorong terciptanya sistem penjaminan mutu internal yang tidak hanya memenuhi Standar Nasional Pendidikan Tinggi tetapi juga mampu bersaing dalam tataran global. 
            VMTS ini diharapkan menjadi landasan kuat bagi DP3M untuk berperan sebagai motor penggerak peningkatan mutu yang mendukung tercapainya visi Universitas Sriwijaya sebagai 
            PTN-BH yang terkemuka, mandiri, unggul, kreatif, dan inovatif dalam berbagai bidang ilmu pengetahuan dan teknologi.
        </p>

