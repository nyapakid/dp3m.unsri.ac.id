        <h3>Audit Mutu Internal</h3>
        <br>
        <h5 class="text-block text-justify">
            Evaluasi pemenuhan standar untuk memastikan semua program studi di UNSRI menjalankan proses pembelajaran dengan konsisten, efektif dan efisien dilaksanakan 
            melalui penilaian Evaluasi Diri Program Studi (EDPS) dan Audit Mutu Internal (AMI) secara periodik setiap 1 tahun sekali. Proses EDPS dan AMI dijalankan 
            secara sistematik, mandiri, dan objektif, dengan tujuan mengetahui kesesuaian atau ketidaksesuaian terhadap persyaratan sistem manajemen mutu dan regulasi 
            yang berlaku, mengevaluasi kapabilitas serta efektivitas penerapan sistem tersebut, dan mengidentifikasi peluang perbaikan yang dapat mendorong peningkatan 
            mutu pendidikan secara berkelanjutan.
            <br>
            <br>
            Tahapan pelaksanaan AMI mencakup perencanaan, pelaksanaan audit, pemeriksaan/evaluasi, dan tindak lanjut atas temuan audit. UNSRI menekankan bahwa tindak 
            lanjut yang sistematis merupakan kunci keberhasilan untuk memperbaiki dan meningkatkan proses pembelajaran secara berkelanjutan. Pelaksanaan AMI tidak hanya 
            bersifat formalitas untuk pemenuhan syarat administratif, namun hasilnya dapat menjadi suatu early warning system yang bermanfaat untuk pemantauan capaian 
            kinerja dan peningkatan perbaikan secara nyata. UNSRI memiliki kurang lebih 144  Auditor Mutu Internal aktif yang bekerja secara independen dan memberikan 
            pendapat atas kewajaran laporan evaluasi diri yang diauditnya sesuai lingkup audit yang telah ditetapkan dengan tujuan memberikan ruang peningkatan Program 
            Studi.
            <br>
            <br>    
        </h5>

