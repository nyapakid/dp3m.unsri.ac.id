        <article class="quote-classic">
            <h2 class="quote-classic-cite">Karakteristik SPMI</h2>
            <div class="quote-classic-text">
                <p class="q text-justify">Pengembangan dan penerapan SPMI Universitas Sriwijaya periode 2019-2025 berkarakteristik integratif dan berbasis teknologi 
                    informasi. Hal ini bermakna bahwa PMI mendorong keterkaitan, keterhubungan yang harmonis dan saling menguatkan antarunit kerja untuk menyelenggarakan
                     pendidikan tinggi bermutu di Universitas Sriwijaya. Sistem ini diterapkan dengan memaksimalkan pemanfaatan teknologi informasi dalam siklus PPEPP 
                     Standar Universitas Sriwijaya.
                </p>
            </div>
        </article>

        <article class="quote-classic">
            <h2 class="quote-classic-cite">Organisasi SPMI</h2>
            <div class="quote-classic-text">
                <p class="q text-justify">SPMI di Universitas Sriwijaya harus dikembangkan dan diterapkan oleh LP3MP yang kompeten, berwenang, dan memiliki sumberdaya 
                    yang kuat. LP3MP berwenang dan didukung sumber daya yang memadai untuk mendorong perbaikan, peningkatan mutu kerja segenap individu dan unit kerja 
                    di Universitas Sriwijaya. Organisasi pelaksana SPMI disusun terpusat di Universitas Sriwijaya sesuai Peraturan Menteri Riset, Teknologi dan 
                    Pendidikan Tinggi Nomor 12 Tahun 2015 tentang Organisasi Tata Kerja Universitas Sriwijaya.<br>
                    Setiap individu dan unit kerja di Universitas Sriwijaya secara intrinsic wajib menerapkan cara kerja untuk menjami mutu dalam pelaksanaan tugas dan 
                    fungsinya. SPMI yang diterapkan oleh setiap unit kerja berlandaskan asas dan prinsip yang telah ditetapkan. Keseluruhan upaya penjaminan mutu ini 
                    ditujukan untuk mewujudkan peningkatan mutu berkelanjutan (Continuous Quality Improvement) yang akan menciptakan budaya mutu di Universitas Sriwijaya.
                </p>
            </div>
        </article>

        <article class="quote-classic">
            <h2 class="quote-classic-cite">Managemen SPMI</h2>
            <div class="quote-classic-text">
                <p class="q text-justify">Manajemen SPMI Universitas Sriwijaya harus dilakukan secara profesional, berdasarkan prinsip dan azas yang telah ditetapkan. 
                    Penjaminan Mutu Universitas Sriwijaya dilaksanakan secara konsisten dan berkelanjutan untuk menjamin kepuasan pelanggan dan seluruh pemangku 
                    kepentingan (stakeholders) Universitas  Sriwijaya  serta  menjamin  penyelenggaraan  Pendidikan Tinggi bermutu di Universitas Sriwijaya. 
                    Manajemen SPMI menjamin agar siklus penetapan, pelaksanaan, evaluasi, pengendalian dan peningkatan (PPEPP) standar mutu Universitas Sriwijaya 
                    dilaksanakan secara efisien, efektif dan berkelanjutan.
                </p>
            </div>
